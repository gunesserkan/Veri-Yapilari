    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //polinomun hesaplanıp GUI üzerinde gösterilmesi için buton kodları
        if (jTextField2.getText() != "") {
            int toplam = 0;
            int xDegeri = Integer.parseInt(jTextField2.getText());
            int[][] degerler = polinom.degerler();
            for (int i = 0; i < polinom.getAdet(); i++) {
//                System.out.println(degerler[i][0] + " " + degerler[i][1]);
                toplam += degerler[i][0]*(Math.pow(xDegeri, degerler[i][1]));
            }
            jLabel4.setText("Polinom sonucu : " + toplam);
        } else {
            System.out.println("X değeri girin");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Polinoma yeni düğüm eklemek için buton komutları
        String plnm, a = "";
        int katSayi = 1, us = 1;
        boolean sira = true;
        //polinome yeni eklenecek değerin belirlenen maksimum uzunluktan fazla olması ör:[-2x^3] 5 karakterli
        if (jTextField1.getText().length() > 5) {
            jTextField1.setText("Çok fazla değer girdiniz!!");
        } //girilen değer belirlenen aralıkta ama kat sayısı yoksa çalışacak kısım
        else if (jTextField1.getText().length() == 4 && jTextField1.getText().charAt(0) == '-' || jTextField1.getText().charAt(0) == '+') {
            if (jTextField1.getText().charAt(0) == '-') {
                katSayi = -1;
                a += jTextField1.getText().charAt(3);
                us = Integer.parseInt(a);
            } else if (jTextField1.getText().charAt(0) == '+') {
                katSayi = 1;
                a += jTextField1.getText().charAt(3);
                us = Integer.parseInt(a);
            }
        } //girilen değer kat sayısız ve işaretsiz ise çalışacak kısım
        else if (jTextField1.getText().length() == 3) {
            if (jTextField1.getText().charAt(0) == 'x' || jTextField1.getText().charAt(0) == 'X') {
                katSayi = 1;
                a += jTextField1.getText().charAt(2);
                us = Integer.parseInt(a);
            }

        } 
        else if(jTextField1.getText().length()==2&&jTextField1.getText().charAt(0)=='-'){
            if(jTextField1.getText().charAt(1)=='x'||jTextField1.getText().charAt(1)=='X')
            {
                katSayi=-1;
                us=1;
            }else if(jTextField1.getText().charAt(1)>='1'&&jTextField1.getText().charAt(1)<='9'){
                a += jTextField1.getText().charAt(1);
                katSayi=Integer.parseInt(a)*-1;
                us=1;
            }
        }else if(jTextField1.getText().length()==2&&jTextField1.getText().charAt(0)=='+'){
            if(jTextField1.getText().charAt(1)=='x'||jTextField1.getText().charAt(1)=='X')
            {
                katSayi=1;
                us=1;
            }else if(jTextField1.getText().charAt(1)>='1'&&jTextField1.getText().charAt(1)<='9'){
                a += jTextField1.getText().charAt(1);
                katSayi=Integer.parseInt(a);
                us=1;
            }
        }
        else if(jTextField1.getText().length()==1&&jTextField1.getText().charAt(0)>='1'&&jTextField1.getText().charAt(0)<='9'){
            a += jTextField1.getText().charAt(0);
            katSayi=Integer.parseInt(a);
            us=0;
            
        }
            //girilen değerin<=5 karakter olduğu spesifik olmayan tüm durumlar için
        else {
            plnm = jTextField1.getText();
            a = "";
            for (int i = 0; i < plnm.length(); i++) {
                if (plnm.charAt(i) >= '1' && plnm.charAt(i) <= '9') {
                    if (sira == true) {
                        a += plnm.charAt(i);
                        katSayi = Integer.parseInt(a);
                        a = "";
                        sira = false;
                    } else {
                        a += plnm.charAt(i);
                        us = Integer.parseInt(a);
                        a = "";
                        sira = true;
                    }
                } else if (plnm.charAt(i) == '-') {
                    if (sira == true) {
                        a += plnm.charAt(i + 1);
                        katSayi = (Integer.parseInt(a) * -1);
                        a = "";
                        sira = false;
                    } else {
                        a += plnm.charAt(i + 1);
                        us = (Integer.parseInt(a) * -1);
                        a = "";
                        sira = true;
                    }
                    i++;
                } else if (plnm.charAt(i) == '+') {
                    if (sira == true) {
                        a += plnm.charAt(i + 1);
                        katSayi = Integer.parseInt(a);
                        a = "";
                        sira = false;
                    } else {
                        a += plnm.charAt(i + 1);
                        us = Integer.parseInt(a);
                        a = "";
                        sira = true;
                    }
                    i++;
                }

            }
        }
        //yeni değer listeye eklendikten sonra çalışan komutlar
//        System.out.println(katSayi+" "+us);
        polinom.add(katSayi, us);
        jLabel3.setText(polinom.yaz());
        jTextField1.setText("");
        sira = true;
        a = "";


    }//GEN-LAST:event_jButton2ActionPerformed


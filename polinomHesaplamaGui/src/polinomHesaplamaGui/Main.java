    public boolean usKontrol(int[][] degerler){
        int fark=degerler[0][1]-degerler[1][1];
        int farkKontrol;
        for(int i=0;i<polinom.getAdet()-1;i++){
            farkKontrol=degerler[i][1]-degerler[i+1][1];
            if(fark!=farkKontrol||degerler[i][1]==0)
                return false;
        }
        return true;
    }
